    private void listar_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listar_buttonActionPerformed
        try {
            Map<String, Object> list_resp = this.c.list();
            StringBuilder ans = new StringBuilder();

            ans.append("cmd: ");
            ans.append((String) list_resp.get("cmd")).append("\n");

            ans.append("place: ");
            ans.append(((ArrayList<String>) list_resp.get("place")).toString());

            this.listagem_textarea.setText(ans.toString());
            this.logger.append("Listagem \n");
            this.logger_textarea.setText(logger.toString());

        } catch (IOException e) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, String.format("Erro na Listagem: ", e));
        }
    }//GEN-LAST:event_listar_buttonActionPerformed

    private void atualizar_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizar_buttonActionPerformed
        try {
            String locate = this.dispositivo_textfield.getText();
            String value = this.novo_valor_textfield.getText();

            if (locate.isBlank() || value.isBlank()) {
                throw new Exception("Campos invalidos para atualização");
            }

            Map<String, Object> get_resp = this.c.set(locate, value);
            StringBuilder ans = new StringBuilder();

            ans.append("cmd: ");
            ans.append((String) get_resp.get("cmd")).append("\n");

            ans.append("locate: ");
            ans.append((String) get_resp.get("locate")).append("\n");

            ans.append("value: ");
            ans.append((String) get_resp.get("value")).append("\n");

            this.listagem_textarea.setText(ans.toString());
            this.logger.append("Atualização de dispositivo: ").append(locate).append("\n");
            this.logger_textarea.setText(logger.toString());

        } catch (Exception e) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(this, String.format("Erro na atualização: ", e));
        }
    }//GEN-LAST:event_atualizar_buttonActionPerformed

    private void iniciar_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciar_buttonActionPerformed
        final int seconds = Integer.parseInt(this.listagem_automatica_textfield.getText()) * 1000;
        this.running = true;
        
        this.atualization_thread = new Thread(() -> {
            try {
                while (running) {
                    this.get("all");
                    Thread.sleep(seconds);
                }
            } catch (InterruptedException | IOException e) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, e);
                JOptionPane.showMessageDialog(this, String.format("Erro na listagem automática: ", e));
            }
        }
        );
        atualization_thread.start();
    }//GEN-LAST:event_iniciar_buttonActionPerformed

