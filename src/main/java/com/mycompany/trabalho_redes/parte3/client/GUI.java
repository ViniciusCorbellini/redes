    private void get(String place) throws IOException {
        Map<String, Object> get_resp = this.c.get(place);
        StringBuilder ans = new StringBuilder();

        ans.append("cmd: ");
        ans.append((String) get_resp.get("cmd")).append("\n");

        ans.append("place: ");
        ans.append(get_resp.get("place").toString()).append("\n");

        ans.append("value: ");
        ans.append(get_resp.get("value").toString());

        this.listagem_textarea.setText(ans.toString());
        this.logger.append("Requisicao de busca para o place: ").append("all\n");
        this.logger_textarea.setText(logger.toString());
    }

